    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat ("yyyy-MM-dd");
        String pinjam = dateFormat.format(txtpj.getDate());
        String kembali = dateFormat.format(txtkb.getDate());
        if(txtnama.getText().equals("") || 
           txtalamat.getText().equals("") ||
           txtnopol.getText().equals("") ||
           pinjam.equals("") ||
           kembali.equals("")){
                    JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            String SQL = "INSERT INTO tb_pinjam VALUES (NULL,'"+txtnama.getText()+"','"+txtalamat.getText()+"','"+txtnopol.getText()+"','"+pinjam+"','"+kembali+"','"+txtharga.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            if(status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Sukses", JOptionPane.WARNING_MESSAGE);
            }
                      
        
        }

    }//GEN-LAST:event_btnsaveActionPerformed

    private void selectData(){
       String kolom[] = {"No Struk","Nama","Alamat","No Polisi","Pinjam","Kembali","Harga"};
       DefaultTableModel dtm = new DefaultTableModel(null, kolom);
       String SQL = "SELECT * FROM tb_pinjam";
       ResultSet rs = KoneksiDB.executeQuery(SQL);
       try{
           while(rs.next()){
               String NoStruk = rs.getString(1);
               String Nama = rs.getString(2);
               String Alamat = rs.getString(3);
               String NoPol = rs.getString(4);
               String TglPinjam = rs.getString(5);
               String TglKembali = rs.getString(6);
               String Harga = rs.getString(7);
               String data [] = {NoStruk,Nama,Alamat,NoPol,TglPinjam,TglKembali,Harga};
               dtm.addRow(data);
           }   
        }catch(SQLException ex){
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
       tblData.setModel(dtm);
    }
    
