    private void hargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hargaActionPerformed
        // TODO add your handling code here:
        long startTime = txtpj.getDate().getTime();
            long endTime = txtkb.getDate().getTime();
            
            long sewaTime = (endTime - startTime);
            long bedaHari = sewaTime/(1000 * 60 * 60 * 24);
            
            Integer harga1 = Integer.parseInt(String.valueOf(bedaHari));
            Integer hargatotal  = harga1 * 20000;
            txtharga.setText("Rp "+hargatotal);
    }//GEN-LAST:event_hargaActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int baris = tblData.getSelectedRow();
        int confirm = JOptionPane.showConfirmDialog(null,"Anda yakin ingin menghapus data?","Confirmation", JOptionPane.YES_NO_OPTION);
        if (confirm == 0){
            if(baris != -1){
                String NoStruk = tblData.getValueAt(baris, 0).toString();
                String SQL = "DELETE FROM tb_pinjam WHERE NoStruk='"+NoStruk+"'";
                int status = KoneksiDB.execute(SQL);
                
                if(status==1){
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    selectData();
                } else {
                    JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
                }
            } else {
               JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih Dahulu", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        // TODO add your handling code here:
        int baris = tblData.getSelectedRow();
        if(baris != -1){
            txtnama.setText(tblData.getValueAt(baris, 1).toString());
            txtalamat.setText(tblData.getValueAt(baris, 2).toString());
            txtnopol.setText(tblData.getValueAt(baris, 3).toString());
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateFormat = null;
            Date dateFormat1 = null;
            try {
                dateFormat = date.parse(tblData.getValueAt(baris, 4).toString());
                dateFormat1 = date.parse(tblData.getValueAt(baris, 5).toString());
            } catch (ParseException ex) {
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtpj.setDate(dateFormat);
            txtkb.setDate(dateFormat1);
            txtharga.setText(tblData.getValueAt(baris, 6).toString());
        }
    }//GEN-LAST:event_tblDataMouseClicked

